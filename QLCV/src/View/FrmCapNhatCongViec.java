    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:

        String ma = cbxMacv.getSelectedItem().toString();
        try{
            if (ma.length() ==0) {
                JOptionPane.showMessageDialog (null, "Cần chọn 1 sản phảm xóa", "Thông báo", 1);
            }else{
                if(JOptionPane.showConfirmDialog (null, "Bạn có muốn xóa sản phẩm " + ma + " này hay không?", "Thông báo", 2) == 0){
                    ct.DeleteData(ma);
                    ClearData();
                    ShowData();
                    setNull();
                }
            }

        }catch(SQLException ex){ Logger.getLogger(FrmCapNhatCongViec.class.getName()).log(Level.SEVERE, null, ex);}
    }//GEN-LAST:event_btnXoaActionPerformed

