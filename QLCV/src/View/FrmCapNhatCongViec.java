    // Ham do du lieu vao combobox
    public final void ShowDataCombo(){
        ResultSet result = null;
        
        try{
             result = ct.ShowCongviec();
            while (result.next()){
                cbxMacv.addItem(result.getString("MACV"));
            }
        }catch (SQLException e){
        }
    }

    // Hien thi ten loại theo ma loai trong combobox 
    public void ShowTenloai (String macv) throws SQLException {
        ResultSet result = ct.ShowCongViec(macv);
        if (result.next ()) { // nếu còn đọc tiếp được một dòng dữ liệu
            txtTencv.setText (result.getString("TENCV"));
        }
    }
    

     public final void ShowData() throws SQLException {
        ResultSet result = null; 
        result = ct.ShowCTCongviec();                 
        try{
            ClearData();
            while (result.next()) {
                String rows[] = new String[4];
                rows[0] = result.getString(1);
                rows[1] = result.getString(2);
                rows[2] = result.getString(3);
                tableModel.addRow(rows);
            }
        }catch(Exception e){
            
        }
    }
     
     public void ClearData() throws SQLException {
        //Lay chi Bo dong cuoi cung 
        int n = tableModel.getRowCount() - 1; 
        for (int i = n; i >= 0; i--) {
            tableModel.removeRow(i);  //Remove tung dong
        }
    }

     // Ham xoa trang cac TextField 
