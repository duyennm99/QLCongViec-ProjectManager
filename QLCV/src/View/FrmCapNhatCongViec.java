    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:

        String macv = cbxMacv.getSelectedItem().toString();
        String tiendo = cbxTiendo.getSelectedItem().toString();
        double mucdo;
        mucdo = Integer.parseInt(txtMucdo.getText());
        NumberFormat phantram = NumberFormat.getCurrencyInstance();
        String ptmucdo = phantram.format(mucdo);

        if (macv.length() == 0 || tiendo.length() == 0) {
            JOptionPane.showMessageDialog(null,"Vui lòng nhập mã sản phẩm và tên sản phẩm ", "Thông báo", 1);
        } 
        try {
            if (cothem == true){
                ct.InsertData(macv, tiendo, mucdo);
            }
            else{
                ct.InsertData(macv, tiendo, mucdo);
            }

                ClearData();
                ShowData();
        }
        catch(SQLException ex){
                JOptionPane.showMessageDialog(null, "Cập nhật thất bại","Thông báo", 1);
            }
            setNull();
            setKhoa(false);
            setButton(false);
        
    }//GEN-LAST:event_btnLuuActionPerformed

