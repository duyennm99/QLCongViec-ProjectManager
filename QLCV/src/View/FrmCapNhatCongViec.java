    private void tblCTCVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCTCVMouseClicked
        // TODO add your handling code here:
        
        try{
            int row = this.tblCTCV.getSelectedRow();
            String macv = (this.tblCTCV.getModel().getValueAt (row, 0)).toString();
            ResultSet rs = ct.ShowCongViec(macv);
            
            if (rs.next()){
                cbxMacv.setSelectedItem(rs.getString("MACV")); 
                txtTencv.setText (rs.getString("TENCV"));
                cbxTiendo.setSelectedItem(rs.getString("TIENDOCONGVIEC"));
                txtMucdo.setText(rs.getString("MUCDOHOANTHANH"));
                txtTencv.setText(rs.getString("TENCV"));
            }
        } catch(SQLException e){}
    }//GEN-LAST:event_tblCTCVMouseClicked

